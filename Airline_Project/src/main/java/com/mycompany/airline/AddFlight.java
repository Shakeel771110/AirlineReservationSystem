    public void autoID() {
    try {
        Connection con;
        PreparedStatement pre;
        Class.forName("com.mysql.cj.jdbc.Driver"); // use cj for latest versions
        con = DriverManager.getConnection("jdbc:mysql://localhost/Airline_Project", "root", "1234");

        Statement s = con.createStatement();
        ResultSet rs = s.executeQuery("SELECT MAX(FlightID) FROM Flight");
        rs.next();
        if (rs.getString("MAX(FlightID)") == null) {
            flightIdTextField.setText("FL001");
        } else {
            String id = rs.getString("MAX(FlightID)").substring(2);
            long num = Long.parseLong(id);
            num++;
            flightIdTextField.setText("FL" + String.format("%03d", num));
        }

    } catch (ClassNotFoundException ex) {
        Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
    } catch (SQLException ex) {
        Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
    }
}


